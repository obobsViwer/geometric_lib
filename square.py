
def area(a):
    """
            Вычисляет площадь квадрата

            Параметры:
              a(int) : длина стороны квадрата

            Возвращаемое значение:
              int : площадь квадрата

        """
    return a * a


def perimeter(a):
    """
        Вычисляет периметр квадрата

        Параметры:
          a(int) : длина стороны квадрата

        Возвращаемое значение:
          int : периметр квадрата

    """
    return 4 * a
